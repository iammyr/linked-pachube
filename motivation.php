<h3>WHAT is this</h3>
Linked Data (unambiguous semantics and meaningful links) on sensors creatable by and for any kind of user.
No expertise required. 
Currently real sensor data and metadata are taken from <a href = "https://pachube.com/" title = "Pachube">Pachube</a>.
<h3>WHY</h3>
Sensors do neither interoperate nor recognize each other. 
Because they speak different languages (schema). Unambiguous semantics applied on them will help them
to
<ul>
<li>meaningfully integrate them with each other</li>
<li>better contextualize them (by meaningfully linking them with additional content)</li>
</ul> 
